package com.amnah.ali.cotton.util

import com.github.mikephil.charting.components.AxisBase
import com.github.mikephil.charting.formatter.ValueFormatter

class MyValueFormatter {

    class MyValueFormatter(private val _xValsDateLabel: ArrayList<String>) : ValueFormatter() {
        override fun getFormattedValue(value: Float): String {
            return value.toString()
        }
        override fun getAxisLabel(value: Float, axis: AxisBase): String {
            return if (value.toInt() >= 0 && value.toInt() <= _xValsDateLabel.size - 1) {
                _xValsDateLabel[value.toInt()]
            } else {
                ("").toString()
            }
        }
    }
}